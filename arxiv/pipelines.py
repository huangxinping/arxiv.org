    def process_item(self, item, spider):
        doc = {
            'url': item['url'],
            'title': item['title'],
            'chinese_title': self.translate_with_youdao(item['title']),
            'authors': item['authors'],
            'abstract': item['abstract'],
            'subjects': item['subjects'],
            'submissions': item['submissions'],
            'attachment': item['attachment'],
            'notified': False
        }
        client = pymongo.MongoClient("192.168.0.210", 27017)
        if client.papers.arxiv.count_documents({"url": item['url']}) <= 0:
            client.papers.arxiv.insert_one(doc)
        else:
            query = {"url": item['url'], "chinese_title": ''}
            if client.papers.arxiv.find(query).count() > 0:
                client.papers.arxiv.update_one(
                    {'_id': item['_id']},
                    {"$set": {"chinese_title": self.translate_with_youdao(item['title'])}}
                )
        return item
